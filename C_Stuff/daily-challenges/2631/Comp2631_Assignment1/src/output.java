import java.io.InputStream;
import java.io.PrintStream;
import java.util.Scanner;

/*
 * Purpose: To handle output and input to/from the console
 */
public class output {
	
	/*
	 * Purpose:To print  a string to the console without using system.out
	 * Method: Takes the prinstream from the main program and a string and outputs it to the console 
	 * I/O:Printstream and the string to print
	 */
	public static void print(PrintStream console, String string)
	{
		console.print(string);
	}
	
	
	/*
	 * Purpose:To print  a string to the console without using system.out
	 * Method: Takes the prinstream from the main program and a string and outputs it to the console 
	 * I/O:
	 */
	public static String readString(Scanner scanner)
	{
		scanner = new Scanner(System.in);
		String string;
		
		string = scanner.nextLine();
		
		return string;
	}

}
