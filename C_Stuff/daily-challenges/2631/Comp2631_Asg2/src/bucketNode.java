
public class bucketNode {
	
	private String dataPath;
	private String dataCommand;
	public bucketNode next;
	public bucketNode previous;

	//constructor for all the nodes in the linked list set them equal to null so that we dont have a problem initializing them
	bucketNode()
	{
		this.dataPath = null;
		this.dataCommand = null;
		this.next = null;
		this.previous = null;
	}	
	
	//getters and setters here
	public void setPrevious(bucketNode newPrevious)
	{
		this.previous = newPrevious;
	}
	
	public void setNext(bucketNode newNext)
	{
		this.next = newNext;
	}
	
	public bucketNode getNext()
	{
		return this.next;
	}
	
	public bucketNode getPrevious()
	{
		return this.previous;
	}
	
	public void setData(String s)
	{
		this.dataPath = s;
	}
	
	public void setCommand(String s)
	{
		this.dataCommand = s;
	}
	
	public String getData()
	{
		return this.dataPath;
	}
	
	public String getCommand()
	{
		return this.dataCommand;
	}
	
	
	
	
	
	
}
