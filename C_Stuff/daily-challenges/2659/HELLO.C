#include <stdio.h>


struct S
{
	int i;
	char c;
};


union U
{
	int i;
	char c;
};


enum E { C1, C2, C3, C4 };


int main()
{

	/*remeber to cast everything to the value you want %ld means long decimal*/	
	printf("int: %d\n",(int)sizeof(int));
	printf("Char: %d\n",(int)sizeof(char));
	printf("Short: %d\n",(int)sizeof(short));
	printf("Long: %d\n",(int)sizeof(long));
	printf("S: %d\n",(int)sizeof(struct S));
	printf("Union U: %d\n",(int)sizeof(union U));
	printf("Enum E: %d\n",(int)sizeof(enum E));
	
	
	return 0;
}



